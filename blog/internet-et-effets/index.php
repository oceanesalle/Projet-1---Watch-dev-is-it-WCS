                <p>Internet est désormais bien ancré dans nos vies. Il suffit d’observer notre façon de communiquer, d’entretenir nos relations, et d’obtenir de l’information afin de s’en rendre compte. Mais il reste un aspect qui questionne encore les scientifiques : quels sont les effets d’Internet sur le cerveau humain ?</p>
                <p>Disons que nous pouvons observer des côtés positifs comme négatifs. 
                   En effet, l'accès à l’Internet possède de nombreux avantages : l’accès à l’information à tout moment, la communication devient plus simple et plus rapide, … 
                   En revanche, Le multitâche numérique semble ne pas améliorer nos capacités d’attention, le fait de stocker notre "mémoire factuelle" en ligne nous permettrait de nous focaliser sur des tâches plus ambitieuses, et l’interaction sociale sur internet semble avoir des bons et des mauvais côtés, en fonction de l’âge.</p> 
                <p>Comme l'utilisation en ligne peut avoir autant de mauvais côtés que de bons côtés, les chercheurs rappellent quelques règles de précaution :
                   - réduire le nombre de tâches en ligne
                   - s’engager dans des interactions sociales dans la vie réelle
                   - limiter le temps d’utilisation d’internet pour les enfants
                   - parler aux enfants des risques liés à internet.
                </p>
